-- Soft-delete test records created by integration tests.
-- Run after: ./gradlew :app-core:test
-- psql -h localhost -U cuba -d itpearls -f scripts/cleanup-test-entities.sql

UPDATE ITPEARLS_OPEN_POSITION_COMMENT
SET DELETE_TS = NOW(), DELETED_BY = 'test-cleanup'
WHERE COMMENT_ LIKE 'Test comment %'
  AND DELETE_TS IS NULL;

UPDATE ITPEARLS_OPEN_POSITION_NEWS
SET DELETE_TS = NOW(), DELETED_BY = 'test-cleanup'
WHERE SUBJECT LIKE 'TestNews-%'
  AND DELETE_TS IS NULL;

UPDATE ITPEARLS_RECRUTIES_TASKS
SET DELETE_TS = NOW(), DELETED_BY = 'test-cleanup'
WHERE DELETE_TS IS NULL
  AND OPEN_POSITION_ID IN (
      SELECT ID FROM ITPEARLS_OPEN_POSITION WHERE VACANSY_NAME LIKE 'TestVacancy-%' AND DELETE_TS IS NULL
  );

